-- Total do Bem
SELECT SUM(SN3.N3_VORIG1) AS N3_VORIG1
  FROM SN3010 SN3
 WHERE SN3.D_E_L_E_T_ = ' ' AND SN3.N3_CCONTAB = '132106';

-- Detalhe do movimento
SELECT SUM(N4_VLROC1) FROM SN4010
 WHERE D_E_L_E_T_ = ' ' AND N4_CONTA = '132106' AND N4_DATA BETWEEN '19800101' AND '20130831'
  AND N4_OCORR = '05';
 
-- Item com diferença
SELECT SN3.N3_CBASE, SN4.N4_ITEM, SUM(SN3.N3_VORIG1) AS N3_VORIG1, SUM(SN4.N4_VLROC1) AS N4_VLROC1
  FROM SN3010 SN3
  LEFT JOIN SN4010 SN4 ON SN4.N4_CBASE = SN3.N3_CBASE AND SN4.N4_ITEM = SN3.N3_ITEM AND SN4.N4_OCORR = '05'
 WHERE SN3.D_E_L_E_T_ = ' ' AND SN3.N3_CCONTAB = '132106' 
 GROUP BY SN3.N3_CBASE, SN4.N4_ITEM
 HAVING SUM(SN3.N3_VORIG1) <> SUM(SN4.N4_VLROC1)

-- Lançamentos contábeis 
SELECT LEFT(CT2.CT2_DATA, 6), SUM(CT2.CT2_VALOR) AS CT2_VALOR, MIN(SN4.N4_VLROC1) AS N4_VLROC1
  FROM CT2010 CT2
  LEFT JOIN (SELECT N4_CONTA, LEFT(N4_DATA, 6) AS N4_DATA, SUM(N4_VLROC1) AS N4_VLROC1
               FROM SN4010 
              WHERE D_E_L_E_T_ = ' ' AND N4_CONTA = '132106' AND N4_OCORR = '05'
              GROUP BY N4_CONTA, LEFT(N4_DATA, 6)) SN4 ON SN4.N4_CONTA = CT2.CT2_DEBITO 
   AND SN4.N4_DATA = LEFT(CT2.CT2_DATA, 6)
 WHERE CT2.D_E_L_E_T_ = ' ' AND CT2.CT2_DEBITO = '132106'
 GROUP BY LEFT(CT2.CT2_DATA, 6)
 ORDER BY LEFT(CT2.CT2_DATA, 6);
 
SELECT SUM(CT2.CT2_VALOR) AS CT2_VALOR
  FROM CT2010 CT2
 WHERE CT2.D_E_L_E_T_ = ' ' AND CT2.CT2_DEBITO = '132106'

SELECT SUM(CT2.CT2_VALOR) AS CT2_VALOR
  FROM CT2010 CT2
 WHERE CT2.D_E_L_E_T_ = ' ' AND CT2.CT2_CREDIT = '132106'
