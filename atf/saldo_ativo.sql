SELECT SN3.N3_CCUSTO, SUM(N4_VLROC1) AS N4_VLROC1 
  FROM SN4010 SN4
  JOIN SN3010 SN3 ON SN3.D_E_L_E_T_ = ' ' AND SN3.N3_CBASE = SN4.N4_CBASE AND SN3.N3_ITEM = SN4.N4_ITEM
 WHERE SN4.D_E_L_E_T_ = ' ' AND SN4.N4_DATA BETWEEN '20130101' AND '20130131' AND SN4.N4_CONTA = '132206'
GROUP BY SN3.N3_CCUSTO;