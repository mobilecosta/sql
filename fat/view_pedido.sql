-- Visão do Pedidos de Vendas
SELECT * FROM FAT031
WHERE C5_NUM = '005916';

-- Instrução contida na view
SELECT SC5.C5_CONDPAG, SC5.C5_NUM, SC5.C5_CODPED, SC5.C5_TIPO, SC5.C5_PEDCLI, SC5.C5_PROP, SC5.C5_VEND1, SC5.C5_VEND2, SC5.C5_COMIS1, SC5.C5_EMISSAO, 
       SC5.C5_VOLUME1, SC5.C5_MENNOTA, SC6.C6_ITEM, SC6.C6_PRODUTO, SC6.C6_CF, SC6.C6_QTDVEN, SC6.C6_PRUNIT, SC6.C6_PRCVEN, SC6.C6_VALOR, 
       SC6.C6_ENTREG, SC6.C6_OBS, SC6.C6_ICMS, SC6.C6_BASEICM, SC6.C6_VLICM, SC6.C6_BASEIPI, SC6.C6_IPI, SC6.C6_VLIPI, SC6.C6_BASEISS, SC6.C6_VLISS, 
       SB1.B1_DESC, SB1.B1_PESO, SE4.E4_DESCRI, SF4.F4_TEXTO, SA1.A1_NOME, SA1.A1_END, SA1.A1_MUN, SA1.A1_EST, SA1.A1_TEL, SA1.A1_FAX, 
       SA1.A1_ENDCOB, SA1.A1_ENDREC, SA1.A1_CONTATO, SA1.A1_CGC, SA1.A1_INSCR, SA1.A1_CEP, SA2.A2_NOME, SA2.A2_END, SA2.A2_MUN, SA2.A2_DDD, 
       SA2.A2_TEL, SA2.A2_EST, SA2.A2_CEP, SA2.A2_FAX, SA2.A2_CGC, SA2.A2_INSCR, SA3_REP.A3_NOME AS NOME_REP, SA3_VEND.A3_NOME AS NOME_VEND, 
       SA4.A4_NOME, SA4.A4_DDD, SA4.A4_TEL, SA1.A1_DDD
  FROM dbo.SC5_N_DELET AS SC5 
 INNER JOIN dbo.SC6_N_DELET AS SC6 ON SC5.C5_NUM = SC6.C6_NUM 
 INNER JOIN dbo.SB1_N_DELET AS SB1 ON SC6.C6_PRODUTO = SB1.B1_COD 
 INNER JOIN dbo.SF4_N_DELET AS SF4 ON SC6.C6_TES = SF4.F4_CODIGO 
 LEFT OUTER JOIN dbo.SA2_N_DELET AS SA2 ON SC5.C5_CLIENTE = SA2.A2_COD AND SC5.C5_LOJACLI = SA2.A2_LOJA 
 LEFT OUTER JOIN dbo.SA1_N_DELET AS SA1 ON SC5.C5_CLIENTE = SA1.A1_COD AND SC5.C5_LOJACLI = SA1.A1_LOJA 
 LEFT JOIN dbo.SE4_N_DELET AS SE4 ON SC5.C5_CONDPAG = SE4.E4_CODIGO -- INNER 
 INNER JOIN dbo.SA4_N_DELET AS SA4 ON SC5.C5_TRANSP = SA4.A4_COD 
 LEFT OUTER JOIN dbo.SA3_N_DELET AS SA3_REP ON SC5.C5_VEND1 = SA3_REP.A3_COD 
 LEFT OUTER JOIN dbo.SA3_N_DELET AS SA3_VEND ON SC5.C5_VEND2 = SA3_VEND.A3_COD
WHERE SC5.C5_NUM = '005916';