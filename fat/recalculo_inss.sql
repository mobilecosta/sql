SELECT SF2.F2_DOC, SF2.F2_SERIE, MIN(SF2.F2_VALINSS) AS F2_VALINSS, SD2.D2_CF, SD2.D2_COD, MIN(SB1.B1_INSS) AS B1_INSS, SUM(SD2.D2_TOTAL) AS D2_TOTAL, SUM( ROUND((SD2.D2_TOTAL * 1) / 100,2)) AS INSS
    FROM SF2010 SF2
     JOIN SD2010 SD2 ON SD2.D_E_L_E_T_ = ' ' AND SD2.D2_FILIAL = SF2.F2_FILIAL AND SD2.D2_DOC =SF2.F2_DOC AND SD2.D2_SERIE = SF2.F2_SERIE
     JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = ' ' AND SB1.B1_FILIAL = SF2.F2_FILIAL AND SB1.B1_COD = SD2.D2_COD 
   WHERE SF2.D_E_L_E_T_ = ' ' AND SF2.F2_VALINSS > 0
      GROUP BY SF2.F2_DOC, SD2.D2_CF, SF2.F2_SERIE, SD2.D2_COD 
         HAVING NOT SD2.D2_CF IN('5101', '6101', '7101', '5111', '5122', '6118')
 ORDER BY SF2.F2_DOC, SD2.D2_CF, SD2.D2_COD 