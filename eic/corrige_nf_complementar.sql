UPDATE SW6010
   SET W6_NF_COMP = SF1.F1_DOC
  FROM (
SELECT SW6.W6_HAWB, SF1.F1_DOC FROM SF1010 SF1
  JOIN SW6010 SW6 ON SW6.D_E_L_E_T_ = ' ' AND SW6.W6_HAWB = SF1.F1_HAWB 
 WHERE SF1.D_E_L_E_T_ = ' ' AND SF1.F1_TIPO_NF = '2' AND SW6.W6_NF_COMP <> SF1.F1_DOC ) SF1
 WHERE SW6010.D_E_L_E_T_ = ' ' AND SW6010.W6_HAWB = SF1.W6_HAWB;