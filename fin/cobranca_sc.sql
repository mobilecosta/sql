-- 1) PMS003211A - INDUSTRIA E COM DE MOLAS SANTA FE LTDA
UPDATE AFG010 
   SET AFG_XCOB = '2'
 WHERE AFG_PROJET = 'PMS003211A' AND AFG_NUMSC + AFG_ITEMSC IN
(
SELECT C7_NUMSC + C7_ITEMSC FROM SC7010 
 WHERE C7_NUM + C7_ITEM IN (
-- SELECT D1_PEDIDO + D1_ITEM, D1_EMISSAO, D1_TOTAL + (D1_TOTAL * 20 /100) AS COBRANCA, * 
SELECT D1_PEDIDO + D1_ITEM
  FROM SD1010 
 WHERE D1_DOC IN ('000001233', '000001257', '000001328') 
   AND D1_EMISSAO IN ('20120229', '20120311', '20120309', '20120401')
   AND D1_TOTAL IN (303.56, 247.74, 249, 318)
   AND D_E_L_E_T_ = ' ' )
)   
  AND NOT AFG_NUMSC + AFG_ITEMSC IN ('0107310002');
  
-- SCs não cobradas
SELECT C7_NUMSC, C7_ITEMSC, * FROM SC7010 
 WHERE C7_OBS LIKE '%RODRIGO BARRIONUEVO - DE 27/02/12 A 02/03/12%' OR C7_OBS LIKE '%GUSTAVO NISHI - DE 27/02/12 A 02/03/12%'

UPDATE AFG010 SET AFG_XCOB = '2' WHERE AFG_NUMSC + AFG_ITEMSC IN ('0105910004', '0105910003')  

-- 2) PMS002811A - DANA INDUSTRIAS LDA
