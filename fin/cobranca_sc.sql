 WHERE AFG_NUMSC + AFG_ITEMSC IN (
SELECT C7_NUMSC + C7_ITEMSC FROM SC7010 
 WHERE C7_OBS LIKE '%FABIO BITENCOURT - 14/03/12%' OR C7_OBS LIKE '%LEONARDO MONTEIRO - 14/03%'
) 

-- 3) SC: 010731 - Item: 0002
