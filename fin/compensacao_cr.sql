SELECT E1_SITUACA, * FROM SE1010
 WHERE D_E_L_E_T_ = ' ' AND E1_NUM IN ('000006212', '100820120') AND E1_PARCELA IN ('A', ''); 

SELECT DISTINCT E1_FILIAL,E1_PREFIXO,E1_NUM,E1_PARCELA,E1_MOEDA,E1_TIPO,E1_SALDO,E1_EMISSAO,E1_CLIENTE,E1_LOJA,
       E1_NOMCLI,E1_SDACRES,E1_SDDECRE,E1_HIST,E1_PIS,E1_COFINS,E1_CSLL,E1_TXMOEDA,SE1.R_E_C_N_O_ RECSE1, A1_CGC 
  FROM SE1010 SE1 
  LEFT JOIN SA1010 SA1 ON SA1.D_E_L_E_T_ = ' ' AND SA1.A1_COD = SE1.E1_CLIENTE AND SA1.A1_LOJA = SE1.E1_LOJA 
 WHERE SE1.E1_FILIAL >= '  ' AND SE1.E1_FILIAL <= '99' AND SE1.E1_CLIENTE >= '      ' AND SE1.E1_CLIENTE <= 'ZZZZZZ'
   AND SE1.E1_SITUACA NOT IN ('I','J') AND  ( SE1.E1_TIPO IN ('RA ') OR  SE1.E1_TIPO IN ('NCC')) 
   AND SE1.E1_TIPO NOT IN ('ND ','IR-','IN-','IS-','PI-','CF-','CS-','FE-','IV-') AND SE1.E1_SALDO > 0 
   AND SE1.E1_EMISSAO <= '20121129' AND SE1.D_E_L_E_T_ = ' ' AND SA1.D_E_L_E_T_ = ' ' 
 ORDER BY E1_FILIAL,E1_NUM,E1_PREFIXO,E1_PARCELA,E1_TIPO