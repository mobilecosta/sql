-- Correção Situação Tributária do IPI
UPDATE SFT010  
   SET FT_CTIPI = CASE WHEN UPD.F4_CTIPI = '00' AND FT_VALIPI = 0 THEN '03' ELSE UPD.F4_CTIPI END
  FROM (
SELECT SFT.R_E_C_N_O_, SF4.F4_CTIPI 
  FROM SFT010 SFT 
  JOIN SD1010 SD1 ON SD1.D1_FILIAL = SFT.FT_FILIAL AND SD1.D1_DOC = SFT.FT_NFISCAL 
   AND SD1.D1_SERIE = SFT.FT_SERIE AND SD1.D1_ITEM = SFT.FT_ITEM AND SD1.D_E_L_E_T_ = ' '
  JOIN SF4010 SF4 ON SF4.F4_FILIAL = SFT.FT_FILIAL AND SF4.D_E_L_E_T_ = ' ' AND SF4.F4_CODIGO = SD1.D1_TES 
   AND SF4.F4_CTIPI <> ''
 WHERE SFT.FT_FILIAL = '01' AND SFT.D_E_L_E_T_ = ' ' AND SFT.FT_CFOP < '5' AND SFT.FT_CTIPI > '49' 
) UPD 
WHERE SFT010.R_E_C_N_O_ = UPD.R_E_C_N_O_;

SELECT CASE WHEN UPD.F4_CTIPI = '00' AND FT_VALIPI = 0 THEN '03' ELSE UPD.F4_CTIPI END AS FT_CTIPI, 
       *
  FROM (
SELECT SF4.F4_CTIPI, SFT.*
  FROM SFT010 SFT 
  JOIN SD1010 SD1 ON SD1.D1_FILIAL = SFT.FT_FILIAL AND SD1.D1_DOC = SFT.FT_NFISCAL 
   AND SD1.D1_SERIE = SFT.FT_SERIE AND SD1.D1_ITEM = SFT.FT_ITEM AND SD1.D_E_L_E_T_ = ' '
  JOIN SF4010 SF4 ON SF4.F4_FILIAL = SFT.FT_FILIAL AND SF4.D_E_L_E_T_ = ' ' AND SF4.F4_CODIGO = SD1.D1_TES 
   AND SF4.F4_CTIPI <> ''
 WHERE SFT.FT_FILIAL = '01' AND SFT.D_E_L_E_T_ = ' ' AND SFT.FT_CFOP < '5' AND SFT.FT_CTIPI > '49' 
) UPD 
ORDER BY FT_ENTRADA

