 WHERE SB9.D_E_L_E_T_ = ' ' AND B9_DATA = '20121231' AND SB9.B9_VINI1 <> 0
   AND COALESCE(INV.TOTAL, 0) <> SB9.B9_VINI1
 ORDER BY SB1.B1_TIPO, SB1.B1_COD; 

