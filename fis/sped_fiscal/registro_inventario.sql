SELECT SUM(TOTAL)
  FROM
(  
SELECT SB1.B1_TIPO, SUM(SB9.B9_VINI1) AS TOTAL
  FROM SB9010 SB9
  JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = ' ' AND SB1.B1_COD = SB9.B9_COD 
 WHERE SB9.D_E_L_E_T_ = ' ' AND B9_DATA = '20121231'
 GROUP BY SB1.B1_TIPO
HAVING SUM(SB9.B9_VINI1) <> 0
-- ORDER BY SB1.B1_TIPO 
) AS TOT 
 
-- Detalhado comparando
