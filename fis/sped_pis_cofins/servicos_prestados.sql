SELECT *
  FROM SFT010 
 WHERE D_E_L_E_T_ = ' ' and FT_ENTRADA between '20120101' and '20120131' 
   AND (FT_SERIE = 'A' OR FT_ESPECIE IN ('NFPS', 'NFS', 'RPS'));

-- Prestados
SELECT * FROM SF2010 
 WHERE D_E_L_E_T_ = ' ' AND F2_SERIE = 'A' 
   AND F2_EMISSAO BETWEEN '20120101' and '20120131' 
 ORDER BY F2_DOC;
 
