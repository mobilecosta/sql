SELECT F2_SERIE, F2_DOC, F2_EMISSAO, F2_VALBRUT, (SELECT MIN(D2_TES) FROM SD2010
                                                   WHERE D_E_L_E_T_ = ' ' AND D2_SERIE = SF2010.F2_SERIE 
                                                     AND D2_DOC = SF2010.F2_DOC AND D2_TES IN ('729', '783') 
                                                     AND D_E_L_E_T_ = ' ')
  FROM SF2010
 WHERE D_E_L_E_T_ = ' ' AND F2_SERIE + F2_DOC IN (
SELECT D2_SERIE + D2_DOC
   FROM SD2010
  WHERE D_E_L_E_T_ = ' ' AND D2_TES IN ('729', '783') AND D_E_L_E_T_ = ' '   
  GROUP BY D2_SERIE + D2_DOC
 )
  ORDER BY F2_EMISSAO;
  
