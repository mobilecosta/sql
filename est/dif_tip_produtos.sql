-- Tipos de produto com diferença entre cadastro e movimento
SELECT SD2.D2_EMISSAO, SD2.D2_TP, SB1.B1_TIPO, * FROM SD2010 SD2
  LEFT JOIN SB1010 SB1 ON SB1.D_E_L_E_T_ = ' ' AND SB1.B1_COD = SD2.D2_COD 
 WHERE SD2.D_E_L_E_T_ = ' ' AND SD2.D2_EMISSAO >= '20110101' AND SD2.D2_EMISSAO <= '20111031'
   AND SB1.B1_TIPO <> SD2.D2_TP
 ORDER BY SD2.D2_EMISSAO;