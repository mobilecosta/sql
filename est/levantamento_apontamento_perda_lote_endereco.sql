SELECT SD7.*,
       SB1.B1_DESC, SD3.D3_COD, SD3.D3_LOTECTL, SD3.D3_DTVALID, SB5.B5_EAN141, 
       SD3.D3_QUANT - 
       COALESCE((SELECT SUM(D3_QUANT) AS D3_QUANT FROM SD3990 
                  WHERE D_E_L_E_T_ = ' ' AND D3_COD = SD3.D3_COD AND D3_LOTECTL = SD3.D3_LOTECTL AND D3_TM > '500' 
                    AND D3_ESTORNO = ' ' AND D3_IDENT = ' '), 0) AS D3_QUANT, SD3.D3_UM, SD3.D3_NUMSEQ, 
       CASE WHEN NOT SD7.D7_NUMERO IS NULL THEN SD7.D7_LOCDEST ELSE SD3.D3_LOCAL END AS D3_LOCAL, SD3.D3_OP, 
       SD7.D7_NUMERO, '' AS CB0_CODETI, '' AS CB0_VOLUME 
  FROM SD3990 SD3 
  JOIN SB1990 SB1 ON SB1.D_E_L_E_T_ = ' ' AND SB1.B1_FILIAL = '01' AND SB1.B1_COD = SD3.D3_COD AND SB1.B1_LOCALIZ = 'S' 
  LEFT JOIN SB5990 SB5 ON SB5.D_E_L_E_T_ = ' ' AND SB5.B5_FILIAL = '01' AND SB5.B5_COD = SD3.D3_COD 
  LEFT JOIN SD7990 SD7 ON SB1.D_E_L_E_T_ = ' ' AND SD7.D7_FILIAL = SD3.D3_FILIAL AND SD7.D7_NUMSEQ = SD3.D3_NUMSEQ 
   AND SD7.D7_TIPO = 0 
 WHERE SD3.D3_FILIAL = '01' AND SD3.D_E_L_E_T_ = ' ' AND SD3.D3_NUMSEQ = '000456' AND SD3.D3_CF = 'PR0';
 
